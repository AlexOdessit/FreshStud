//Sum
let num1 = 40;
let num2 = 60;
let sum = num1 + num2;
console.log(sum);

//Difference
let differ = sum - 50;
console.log(differ);

//multiplication
let product = sum * differ;
console.log(product);

//division
let quotient = product / 2.687;
console.log(quotient);

//remainder
let remainder = product % 2.687;
console.log(remainder);

// to boolean
let boolean = !!product;
console.log(boolean);
